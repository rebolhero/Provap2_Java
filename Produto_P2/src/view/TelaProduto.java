    private void btnbuscarimagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarimagemActionPerformed
        JFileChooser chooser = new JFileChooser();
        File f = null;
        try {
            f = new File(new File("filename.txt").getCanonicalPath());
        } 
        catch (IOException ex){
            Logger.getLogger(TelaProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        chooser.setSelectedFile(f);
        chooser.showOpenDialog(null);
        File curFile = chooser.getSelectedFile();
               
        
        txtimagem.setText(""+curFile.toString());
        
        ImageIcon icon = new ImageIcon(curFile.toString());
        
        lbimagem.setIcon(icon);
    }//GEN-LAST:event_btnbuscarimagemActionPerformed

