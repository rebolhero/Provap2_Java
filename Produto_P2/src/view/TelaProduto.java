    private void listarProdutos(){
    
        try {
            
            DefaultTableModel tbmodelo = (DefaultTableModel) tbproduto.getModel();
            
            tbmodelo.setNumRows(0);
            
            ArrayList<ModelProduto> lista = dproduto.getListaProdutoDAO();
            
            for(int i=0; i<lista.size(); i++){
                tbmodelo.addRow(new Object[]{
                    lista.get(i).getId(),
                    lista.get(i).getCod(),
                    lista.get(i).getStatus(),
                    lista.get(i).getNome(),
                    lista.get(i).getDescricao(),
                    lista.get(i).getQtd_estoque(),
                    lista.get(i).getEstoque_minimo(),
                    lista.get(i).getEstoque_maximo(),
                    lista.get(i).getPreco_compra(),
                    lista.get(i).getPreco_venda(),
                    lista.get(i).getBar_code(),
                    lista.get(i).getNcm(),
                    lista.get(i).getFator(),
                    lista.get(i).getData_cadastro(),
                    lista.get(i).getImagem()
                        
                
                });
            
            }
            
        } 
        catch (Exception erro) {
            
            JOptionPane.showMessageDialog(null,"Algo deu Errado na VIEW"+erro);
        }
    
    }
    
